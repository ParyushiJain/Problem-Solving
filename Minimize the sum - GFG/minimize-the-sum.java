class Solution {
    long minimizeSum(int N, int arr[]) {
        // code here
        PriorityQueue<Integer> q=new PriorityQueue<>();
        for(int i:arr) q.add(i);
        int s=0;
        while(q.size()>1){
            int f=q.poll();
            int sc=q.poll();
            s+=(f+sc);
            q.add(f+sc);
        }
        return s;
    }
}

